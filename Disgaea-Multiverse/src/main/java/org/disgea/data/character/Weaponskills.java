package org.disgea.data.character;

public class Weaponskills {
	private int sword;
	private int spear;
	private int axe;
	private int fist;
	private int staff;
	private int gun;
	private int bow;
	private int monstera;
	private int monsterb;
	private int armor;
	public int getSword() {
		return sword;
	}
	public void setSword(int sword) {
		this.sword = sword;
	}
	public int getSpear() {
		return spear;
	}
	public void setSpear(int spear) {
		this.spear = spear;
	}
	public int getAxe() {
		return axe;
	}
	public void setAxe(int axe) {
		this.axe = axe;
	}
	public int getFist() {
		return fist;
	}
	public void setFist(int fist) {
		this.fist = fist;
	}
	public int getStaff() {
		return staff;
	}
	public void setStaff(int staff) {
		this.staff = staff;
	}
	public int getGun() {
		return gun;
	}
	public void setGun(int gun) {
		this.gun = gun;
	}
	public int getBow() {
		return bow;
	}
	public void setBow(int bow) {
		this.bow = bow;
	}
	public int getMonstera() {
		return monstera;
	}
	public void setMonstera(int monstera) {
		this.monstera = monstera;
	}
	public int getMonsterb() {
		return monsterb;
	}
	public void setMonsterb(int monsterb) {
		this.monsterb = monsterb;
	}
	public int getArmor() {
		return armor;
	}
	public void setArmor(int armor) {
		this.armor = armor;
	}
	
	

}
